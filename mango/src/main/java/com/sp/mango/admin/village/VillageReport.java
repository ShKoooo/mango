package com.sp.mango.admin.village;

public class VillageReport {
	private String brType;
	private int reportNum;
	private int vnum;
	private String subject;
	private String fullSubject;
	private String reasonName;
	private String rcontent;
	private String regDate;
	private String repChecked;
	private Integer replyNum;
	private int catNum;
	
	public String getBrType() {
		return brType;
	}
	public void setBrType(String brType) {
		this.brType = brType;
	}
	public int getReportNum() {
		return reportNum;
	}
	public void setReportNum(int reportNum) {
		this.reportNum = reportNum;
	}
	public int getVnum() {
		return vnum;
	}
	public void setVnum(int vnum) {
		this.vnum = vnum;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getFullSubject() {
		return fullSubject;
	}
	public void setFullSubject(String fullSubject) {
		this.fullSubject = fullSubject;
	}
	public String getReasonName() {
		return reasonName;
	}
	public void setReasonName(String reasonName) {
		this.reasonName = reasonName;
	}
	public String getRcontent() {
		return rcontent;
	}
	public void setRcontent(String rcontent) {
		this.rcontent = rcontent;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getRepChecked() {
		return repChecked;
	}
	public void setRepChecked(String repChecked) {
		this.repChecked = repChecked;
	}
	public Integer getReplyNum() {
		return replyNum;
	}
	public void setReplyNum(Integer replyNum) {
		this.replyNum = replyNum;
	}
	public int getCatNum() {
		return catNum;
	}
	public void setCatNum(int catNum) {
		this.catNum = catNum;
	}
}
